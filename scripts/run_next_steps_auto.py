import yaml

ROOT = Path(__file__).resolve().parents[1]
PYTHON = Path(sys.executable)
RUN_DIR = ROOT / "outputs/next_steps_auto"
LOG_PATH = RUN_DIR / "next_steps_auto.log"
STATUS_PATH = RUN_DIR / "status.json"


def utc_now() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")


def log(message: str) -> None:
    RUN_DIR.mkdir(parents=True, exist_ok=True)
    line = f"[{utc_now()}] {message}"
    print(line, flush=True)
    with LOG_PATH.open("a", encoding="utf-8") as f:
        f.write(line + "\n")


def read_yaml(path: Path) -> dict:
    return yaml.safe_load(path.read_text(encoding="utf-8"))


def write_yaml(path: Path, data: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(yaml.safe_dump(data, sort_keys=False, allow_unicode=True), encoding="utf-8")


def run_step(name: str, command: list[str], log_file: Path, force: bool = False, expected: Path | None = None) -> str:
    if expected is not None and expected.exists() and not force:
        log(f"{name}: reusing existing artifact {expected}")
        return "reused"
    log(f"{name}: starting")
    log_file.parent.mkdir(parents=True, exist_ok=True)
    with log_file.open("ab") as f:
        f.write(("$ " + " ".join(command) + "\n\n").encode())
        subprocess.run(command, cwd=ROOT, stdout=f, stderr=subprocess.STDOUT, check=True)
    log(f"{name}: finished")
    return "completed"


def output_dir(config_path: Path) -> Path:
    cfg = read_yaml(config_path)
    return ROOT / cfg["train"]["output_dir"]


def checkpoint_for(config_path: Path) -> Path | None:
    out = output_dir(config_path)
    for name in ["best_postprocess.pt", "best.pt"]:
        candidate = out / name
        if candidate.exists():
            return candidate
    return None


def read_pid(path: Path) -> int | None:
    if not path.exists():
        return None
    text = path.read_text(encoding="utf-8", errors="ignore").strip()
    return int(text) if text.isdigit() else None


def alive(pid: int | None) -> bool:
    if pid is None:
        return False
    try:
        stat = subprocess.check_output(["ps", "-p", str(pid), "-o", "stat="], text=True).strip()
        if not stat or stat.startswith("Z"):
            return False
    except subprocess.CalledProcessError:
        return False
    try:
        os.kill(pid, 0)
    except OSError:
        return False
    return True


def latest_metric(out_dir: Path) -> str:
    path = out_dir / "train.log"
    if not path.exists():
        return "no train.log"
    text = path.read_bytes().decode("utf-8", "ignore")
    metrics = [line.strip() for line in re.split(r"[\r\n]+", text) if line.strip().startswith("epoch=")]
    return metrics[-1] if metrics else "training epoch in progress"


def make_config(
    base_path: Path,
    target_path: Path,
    output: str,
    model_name: str | None = None,
    slice_window: int | None = None,
    batch_size: int | None = None,
    sampling: str | None = None,
    loss: dict | None = None,
    augmentation: dict | None = None,
) -> Path:
    cfg = read_yaml(base_path)
    cfg["train"]["output_dir"] = output
    cfg["data"]["valid_fold"] = 0
    cfg["data"]["limit_train_samples"] = None
    cfg["data"]["limit_valid_samples"] = None
    cfg["train"]["resume_from_checkpoint"] = None
    cfg["train"]["postprocess_eval"] = {"enabled": True}
    if model_name is not None:
        cfg["model"]["name"] = model_name
    if slice_window is not None:
        cfg["data"]["slice_window"] = slice_window
        cfg["model"]["in_channels"] = slice_window
    if batch_size is not None:
        cfg["train"]["batch_size"] = batch_size
        cfg["inference"]["batch_size"] = batch_size
    if sampling is not None:
        cfg["data"]["sampling"] = sampling
    if augmentation is not None:
        cfg["data"]["augmentation"] = augmentation
    if loss is not None:
        cfg["loss"] = loss
    cfg["metadata_note"] = "Generated by scripts/run_next_steps_auto.py for next-step fold0 model selection."
    write_yaml(target_path, cfg)
    return target_path


